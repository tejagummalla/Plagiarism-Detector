package edu.neu.ccs.plagiarismdetector.util;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.mail.SimpleMailMessage;
import org.springframework.mail.javamail.JavaMailSender;
import org.springframework.stereotype.Service;

import static edu.neu.ccs.plagiarismdetector.util.Constants.FROM_EMAIL_ID;

/**
 * Class to implement email service
 */
@Service
public class EmailService {

    private final JavaMailSender javaMailSender;

    @Autowired
    public EmailService(JavaMailSender javaMailSender) {
        this.javaMailSender = javaMailSender;
    }

    /**
     * Send an email to the address entered with the conntent
     * specified
     * @param toEmail receiver email id
     * @param subject of the email
     * @param message Sends an email to the reciever using the input toEmail
     *                with the subject in the input and message in the input
     */
    public void sendMail(String toEmail, String subject, String message) {
        SimpleMailMessage mailMessage = new SimpleMailMessage();
        mailMessage.setTo(toEmail);
        mailMessage.setSubject(subject);
        mailMessage.setText(message);
        mailMessage.setFrom(FROM_EMAIL_ID);
        javaMailSender.send(mailMessage);
    }
}